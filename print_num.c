#include "ft_printf.h"

int	print_int(int i)
{
	int	len;

	ft_putnbr_fd(i, 1);
	if (i == 0)
		return (1);
	len = 0;
	if (i < 0)
		len++;
	while (i != 0)
	{
		len++;
		i = i / 10;
	}	
	return (len);
}
